class Employee:
    
    language = "python"
    salary = 10


zufra = Employee()
zufra.name = " princess zufra jahan" #This is an object attribute

print(zufra.name,zufra.salary,zufra.language)

khalil = Employee()
khalil.name = "khalil gadha idiot"
khalil.salary = 100000
print(khalil.name,khalil.salary)

# name is object/instance attribute and salary and language are class attributes as they directly belongs to class
